	private void populateTable() {

		DefaultTableModel table = (DefaultTableModel) tbl.getModel();

		int n = table.getRowCount();
		while (n-- > 0) {
			table.removeRow(n);
		}

//		List<ResearchGroup> groups = this.ws.getResearchGroups().getDir();
		List<Complaint> complaints = this.ws.getComplaintDir().getDir();

		int i = 1;
		for (Complaint c : complaints) {
			Vector<String> row = new Vector<>();
			row.add(c.getTitle());
			row.add(c.getDate());
			row.add(c.getHowToReproduce());
			row.add(c.getAdminResponse() == "NOT_YET_REPLIED" ? "Pending" : c.getAdminResponse());
			row.add(c.getId());

			table.addRow(row);
		}
	}
