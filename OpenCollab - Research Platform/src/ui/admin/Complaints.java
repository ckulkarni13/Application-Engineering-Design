        private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
		int R = tbl.getRowCount();
		int C = tbl.getColumnCount();

		// NUID header is at index 0
		for (int i = 0; i < R; ++i) {
			for (int j = 0; j < C; ++j) {
				String updatedResponse = (String) tbl.getValueAt(i, 3);
				String complaintId = (String) tbl.getValueAt(i, 4);

				Complaint c = ws.getComplaintDir().fetchById(complaintId);

				c.setAdminResponse(updatedResponse);
			}
		}
		populateTable();
		JOptionPane.showMessageDialog(this, "All responses saved!");

        }//GEN-LAST:event_btnSaveActionPerformed


